public class Double_Float{
    public static void main(String[] args) {
        float weight;
        weight = 45.0F;
        double height;
        height = 165;
        System.out.println("体重:" + weight + "kg");
        System.out.println("身長:" + height + "cm");
    }
}

//特別なシーンでない場合はDouble型のほうが厳密な小数計算ができる